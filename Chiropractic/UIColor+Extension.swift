import UIKit

extension UIColor {
    
    static var softBlue: UIColor { get { return UIColor(red: 25/255, green: 118/255, blue: 210/255, alpha: 1.0) } }
    static var softRed: UIColor { get { return UIColor(red: 250/255, green: 110/255, blue: 88/255, alpha: 1.0) } }
    static var softBlack: UIColor { get { return UIColor(red: 30/255, green: 30/255, blue: 30/255, alpha: 30/255) } }
    static var softDarkGray: UIColor { get { return UIColor(red: 60/255, green: 60/255, blue: 60/255, alpha: 1.0) } }
    static var softGray: UIColor { get { return UIColor(red: 90/255, green: 90/255, blue: 90/255, alpha: 1.0) } }
    static var softWhite: UIColor { get { return UIColor(red: 248/255, green: 248/255, blue: 248/255, alpha: 1.0) } }
}
